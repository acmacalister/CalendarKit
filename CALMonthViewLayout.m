#import "CALMonthViewLayout.h"
#import "CALMonthLineView.h"

@implementation CALMonthViewLayout

////////////////////////////////////////////////////////////////////////////////////////////////////
- (instancetype)init
{
    if(self = [super init])
    {
        [self registerClass:[CALMonthLineView class] forDecorationViewOfKind:[CALMonthLineView kind]];
    }
    return self;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)prepareLayout
{
    [super prepareLayout];
}
////////////////////////////////////////////////////////////////////////////////////////////////////
- (NSArray *)layoutAttributesForElementsInRect:(CGRect)rect
{
    NSMutableArray *array = [[super layoutAttributesForElementsInRect:rect] mutableCopy];
    return [array copy];
}
@end
